create database FabricaLimpeza;
use FabricaLimpeza;

create table Clientes(
	codCli integer primary key auto_increment,
	nomeCli varchar(40) not null,
	status varchar(15)
	);

create table Produtos(
	codProd integer primary key auto_increment,
	nomeProd varchar(30) not null,
	categoria varchar(20) not null
);

create table Telefones(
	codCli integer not null,
	telCli varchar(15) not null,
	constraint fkTelCli foreign key (codCli) references Clientes(codCli)
	on delete cascade
	on update cascade
);

create table Pedidos(
	nPedido integer primary key auto_increment,
	codCli integer not null,
	dataPedido date not null,
	constraint fkPedidoCli foreign key (codCli) references Clientes(codCli)
	on update cascade
);

create table ListaProd(
	nPedido integer not null,
	codProd integer not null,
	precoProd decimal(6,2) not null,
	qtdProd integer not null,
	constraint fkPedLista foreign key (nPedido) references Pedidos(nPedido)
	on update cascade,
	constraint fkProdLista foreign key (codProd) references Produtos(codProd)
	on update cascade
);

